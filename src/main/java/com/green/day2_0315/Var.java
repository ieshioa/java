package com.green.day2_0315;

public class Var {
    public static void main(String[] args) {
        int num;

        num = 10; // 대입연산자
        System.out.println(num);

        num = 20; // 리터럴
        System.out.println(num);

        num = 20 + 30;
        System.out.println(num);

        num = 10 * 40;
        System.out.println(num);

        num = 10 / 2;
        System.out.println(num);

        num = 10 - 3;
        System.out.println(num);

        num = 10 % 2;
        System.out.println(num);

        num = 9 % 2;
        System.out.println(num);


    }
}
